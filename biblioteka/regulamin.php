<div  id="paragraph_head_borow">
  Regulamin wypożyczalni
</div>
  <div id="art">§1</div>
  <div id="paragraph_text_comp">
      Z Biblioteki mogą korzystać wszyscy mieszkańcy powiatu poznańskiego. <br>
      Korzystanie z Biblioteki jest bezpłatne<br>
      Przy zapisie zgłaszający się powinien:<br>
      - wypełnić kartę zapisu i zobowiązać się podpisem do przestrzegania regulaminu,<br>
      - w przypadku niepełnoletniego czytelnika, drugą stronę druku wypełniają rodzice bądź opiekun, poświadczający swoimi danymi i podpisem odpowiedzialność za czytelnika,<br>
      Czytelnik zobowiązany jest informować bibliotekę o zmianie miejsca zamieszkania, pracy bądź szkoły. <br>
  </div>
  <div id="art">§2</div>
  <div id="paragraph_text_comp">
       Kaucję od czytelnika pobiera się w następujących przypadkach jeżeli, czytelnik nie jest stałym mieszkańcem Murowanej Gośliny:<br>
      - dla dorosłych jest to 100,00 zł,<br>
      - dla dzieci, młodzieży i studentów – 50,00 zł.<br>
      Zwrot kaucji następuje po oddaniu książek w terminie 7 – dni od zgłoszenia rezygnacji z korzystania ze zbiorów biblioteki,<br>
      Po upływie 6 miesięcy od zaprzestania korzystania z biblioteki, nie odebrana kaucja stanowić będzie dochód biblioteki.<br>
   </div>
  <div id="art">§3</div>
  <div id="paragraph_text_comp">
       Czytelnik winien zgłosić się do bibliotekarza, podać swój numer ewidencyjny bądź nazwisko.<br>
      Wypożyczyć można jednocześnie od 1 do 5 woluminów<br>
      Wpłacający kaucję mogą wypożyczyć od 1 do 2 woluminów.<br>
      Wybrane książki czytelnik rejestruje u dyżurnego bibliotekarza.<br>
      Książki wypożycza się na okres nie dłuższy niż 1 miesiąc.<br>
      Można telefonicznie przedłużyć termin zwrotu książki jeżeli, nie ma na nią zapotrzebowania ze strony innych czytelników.<br>
      Biblioteka może zażądać zwrotu książek przed terminem ustalonym w punkcie 5 jeżeli, stanowią one szczególnie<br>
      Na prośbę czytelnika Biblioteka może sprowadzić w miarę możliwości książki z innych bibliotek. Koszty związane z wypożyczenia mi bibliotecznymi pokrywa czytelnik.<br>
      Bibliotekarz na prośbę czytelnika może rezerwować książki aktualnie wypożyczone przez innych czytelników.<br>
      Wybrane książki czytelnik rejestruje u bibliotekarza. Jemu też zwraca wypożyczone książki. <br>
  </div>
  <div id="art">§4</div>
  <div id="paragraph_text_comp">
  Bibliotekarz udziela informacji o książkach, pomaga w doborze literatury, korzystania z katalogów i wydawnictw informacyjnych.
  </div>
<div id="art">  §5</div>
  <div id="paragraph_text_comp">
   Czytelnik zobowiązany jest zwrócić uwagę na stan książek przed i po wypożyczeniu. Za szkody wynikłe z zagubienia,<br> uszkodzenia lub zniszczenia książki odpowiada czytelnik. Zauważone uszkodzenia należy zgłaszać u bibliotekarza.<br>
<br>
  W przypadku zagubienia bądź zniszczenia książki czytelnik obowiązany jest:<br>
<br>
      odkupić ten sam tytuł,<br>
      odkupić inną przydatną w bibliotece książkę, po ustaleniu z bibliotekarzem<br>
      zapłacić:<br>
      - za książkę wydaną do 1999 roku – cenę inwentarzową + 20 zł.<br>
      - za książkę wydaną po 1999 roku trzykrotną cenę inwentarzową<br>
<br>
  W przypadku dzieł szczególnie cennych, bibliotekarz może ustalić wyższą wartość książek o czym, czytelnik musi być poinformowany.<br>
<br>
  W przypadku zagubienia lub zniszczenia jednego z tomów dzieła wielotomowego, czytelnik ponosi odpowiedzialność za całość dzieła.<br>
   </div>


<div id="art">  §6</div>
  <div id="paragraph_text_comp">
   Za przetrzymanie książek ponad termin określony w § 3 ust.5 poza wyjątkowymi przypadkami, uzgodnionymi z bibliotekarzem, pobiera się opłaty w kwocie 0,10 gr. od tomu za każdą dobę zwłoki.

  Za każde upomnienie pisemne pobiera się opłatę 3-krotnie wyższą od opłaty pocztowej.

  Jeżeli czytelnik mimo upomnień wysyłanych przez bibliotekę odmawia zwrotu książek lub uiszczenia należnej opłaty, Biblioteka dochodzi swoich roszczeń zgodnie z przepisami prawa.
   </div>

<div id="art">  §7</div>
  <div id="paragraph_text_comp">
   Czytelnicy niestosując się do przepisów regulaminu mogą być czasowo, a w szczególnych wypadkach na stałe pozbawiony prawa do korzystania z biblioteki.
  Decyzję w tej sprawie podejmuje dyrektor biblioteki.
  </div>
<div id="art">  §8</div>
  <div id="paragraph_text_comp">
   Od dnia 11.05.2020 roku Biblioteka Publiczna Miasta i Gminy w Murowanej Goślinie prowadzi działalność z zachowaniem następujących zasad:

      Uruchomiona dla użytkowników zostają jedynie wypożyczalnie, z  ograniczeniem wolnego dostępu do księgozbiorów oraz innych przestrzeni.
      Książki będą podawane przez bibliotekarzy zgodnie z życzeniem czytelnika.
      Wszyscy przebywający w bibliotece muszą posiadać maseczki oraz rękawiczki.
      Zaleca się wcześniejszą rezerwację książek drogą telefoniczną, mailową lub poprzez konta elektroniczne.
      Należy pamiętać, by zachować bezpieczną odległość.
      Przy stanowisku może przebywać jedna osoba.
      Na górnym holu mogą przebywać maksymalnie 2 osoby, zachowując odpowiedni odstęp od siebie. Pozostałe osoby oczekują na dolnym holu.
      Przed podejściem do stanowiska bibliotekarza należy zdezynfekować ręce.
      Przyjęte książki zostają poddane 7 dniowej kwarantannie, dlatego czas oczekiwania
      na rezerwacje zwracane przez czytelników będzie się wydłużał.
      Do odwołania czytelnia oraz stanowiska komputerowe pozostają niedostępne
      dla użytkowników.
      Nie ma możliwości wypożyczenia Walizeczki z Goślińskiej Biblioteczki.
      Świadczymy usługi ksero, druk, bindowanie, fax i wydruki (dokumenty do wydruku muszą być zapisane na nośniku pamięci).
      Zmianie ulegają godziny otwarcia.
  </div>
    <div id="art">  §9</div>
  <div id="paragraph_text_comp">
  Wszystkie zasady regulaminu sprzeczne z zasadami zawartymi w §8 przestają obowiązywać do odwołania.
</div>

