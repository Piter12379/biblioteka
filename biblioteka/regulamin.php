  <div  id="paragraph_head_comp">
    Regulamin korzystania ze stanowiska komputerowego
  </div>
  <div id="art">
  I. Warunki korzystania: </div>
  <div id="paragraph_text_comp">
  1.Prawo korzystania ze sprzętu komputerowego mają wszyscy czytelnicy.<br>
  2.Korzystanie ze sprzętu komputerowego jest odpłatne,<br>
  3.Czytelnikowi nie wolno korzysta z własnych nośników informacji bez zgody bibliotekarza (CD-ROM, dyskietka itp.)<br>
  4.Czytelnik jest zobowiązany poinformować bibliotekarza o drukowaniu informacji;<br>
  </div>
  <div id="art">II. Poszanowanie sprzętu komputerowego i nośników informacji:</div>
  <div id="paragraph_text_comp">
  1.Czytelnik jest zobowiązany do poszanowania sprzętu komputerowego oraz szczególnej ostrożności przy korzystaniu z nośników informacji,<br>
  2.Obowiązkiem czytelnika jest zgłoszenie dostrzeżonych uszkodzeń sprzętu komputerowego oraz nośników informacji;<br>
  3.W przypadku stwierdzenia jakichkolwiek uszkodzeń, dyrektor biblioteki może odmówi czasowego, a w szczególnych przypadkach bezterminowego prawa korzystania ze stanowiska komputerowego.<br>
  </div>
  <div id="art">III. Zniszczenie lub uszkodzenie sprzętu komputerowego, nośników informacji:</div>
  <div id="paragraph_text_comp">
  1.Czytelnik odpowiada materialnie za zniszczenie lub uszkodzenie w/w materiałów bibliotecznych,<br>
  2.Za uszkodzenia czytelnik płaci odszkodowanie, którego wysokość określa dyrektor biblioteki, w zależności od stopnia uszkodzenia,<br>
  3.Po otrzymaniu odszkodowania Biblioteka wydaje czytelnikowi pokwitowanie,<br>
  </div>
  <div id="art">IV. Zasady korzystania ze stanowiska komputerowego:</div>
  <div id="paragraph_text_comp">
  1.Czytelnik ma prawo korzystania ze stanowiska komputerowego z dostępem do Internetu przez 1 godzinę,<br>
  2.W przypadku braku oczekujących na korzystanie ze stanowiska, czytelnik może korzystać<br>
  3.kolejną godzinę,<br>
  4.Z opłaty zwolnione są osoby pragnące skorzystać ze strony internetowej Murowanej Gośliny, stron Biuletynu<br> Informacji Publicznej oraz stron administracji wojewódzkiej i powiatowej,<br>
  5.Czytelnik ponosi opłatę za każdą rozpoczętą godzinę korzystania ze stanowiska,<br>
  6.Za korzystanie z komputera przez czas krótszy niż ˝ godziny czytelnik płaci połowę stawki,<br>
  7.Obowiązuje zakaz pobierania z Internetu programów komputerowych i plików bez zgody bibliotekarza,<br>
  8.Wprowadzenie wirusów komputerowych na poszczególne stanowiska, jak również do sieci komputerowej powodować<br>
  9.będzie odpowiedzialność<br>
  10.karną czytelnika, zgodnie z przepisami kodeksu karnego,<br>
  11.Odpowiedzialność<br>
  12.karna nie wyłącza odpowiedzialności materialnej za straty poniesione przez Bibliotekę w związku z uszkodzeniem<br> sprzętu komputerowego, oprogramowania, informacji.<br>
  13.Zabrania się otwierania stron internetowych zawierających informacje o charakterze erotycznym, wulgarnym, rażące uczucia lub dobre obyczaje.<br>
  </div>
  <div id="art">V. Przepisy końcowe:</div>
  <div id="paragraph_text_comp">
  Czytelnik niestosując się do przepisów Regulaminu może być pozbawiony prawa korzystania ze stanowiska komputerowego.<br>
  </div>
