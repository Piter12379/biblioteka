      Regulamin Czytelni Biblioteki Publicznej
    </div>
      <div id="paragraph_text">
 <div id="art">   §1 </div>   Z czytelni mogą korzystać wszyscy obywatele niezależnie od tego czy są stałymi czytelnikami biblioteki.
      Korzystanie z czytelni jest bezpłatne.
      Zgłaszający się pozostawia u bibliotekarza dokument z fotografią.
      Wierzchnie okrycie i teczkę lub torbę należy pozostawić w wyznaczonym miejscu.
      Czytelnik jest zobowiązany wpisać się  do zeszytu obecności.
      W czytelni obowiązuje cisza, zakaz palenia tytoniu i spożywania posiłków.
</div>
<div id="paragraph_text">
 <div id="art">   §2</div>
      W czytelni korzystać można z księgozbioru podręcznego i regionalnego.
      Przyniesione ze sobą książki z innego działu czytelnik zgłasza  bibliotekarzowi z księgozbioru podręcznego i bieżących czasopism, czytelnik korzysta za pośrednictwem bibliotekarza.
      Książek nie wolno czytelnikowi samowolnie wynosić z czytelni.
      przed opuszczeniem czytelni, czytelnik zwraca książki i otrzymuje pozostawiony dokument.
      Książek z księgozbioru nie wypożycza się do domu.
</div>
<div id="paragraph_text">
 <div id="art">   §3</div>
  Czytelnia udziela informacji dotyczących posiadania zbiorów, doboru literatury, sposobu korzystania z katalogu, wydawnictw informacyjnych i innych materiałów znajdujących się w czytelni.
</div>
<div id="paragraph_text">
   <div id="art"> §4</div>
  Na kopiowanie rysunków, schematów, planów, map itp. Czytelnik powinien uzyskać zgodę bibliotekarza.
</div>
<div id="paragraph_text">
 <div id="art">   §5</div>
      Czytelnik zobowiązany jest do poszanowania książek. Korzystanie z rycin, map itp. wymaga szczególnej ostrożności, aby nie narazić ich na uszkodzenie.
      Za uszkodzenie książki czytelnik płaci karę, której wysokość określa dyrektor biblioteki w zależności od stopnia uszkodzenia.
      Na sumy wpłacane z tyt. uszkodzenia książki biblioteka wydaje pokwitowania.
</div>
<div id="paragraph_text">
   <div id="art"> §6</div>
  Skargi i wnioski czytelnicy mogą wpisywać do książki skarg i wniosków
</div>
<div id="paragraph_text">
   <div id="art">§7 </div>
  Czytelnicy nie stosujący się do przepisów regulaminu mogą być czasowo, a szczególnie drastycznych wypadkach na stałe pozbawieni prawa do korzystania z biblioteki.
  Czytelnikowi przysługuje prawo odwołania się do Burmistrza.</div>
</div>
